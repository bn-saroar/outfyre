    <script id="featured-filter-script">
      document.addEventListener("DOMContentLoaded", function () {
        const filterButtons = {
          all: document.getElementById("filter-all-btn"),
          women: document.getElementById("filter-women-btn"),
          men: document.getElementById("filter-men-btn"),
          kids: document.getElementById("filter-kids-btn"),
        };
        const productCards = document.querySelectorAll(".product-card");
        function updateButtonStates(activeButton) {
          Object.values(filterButtons).forEach((button) => {
            button.classList.remove("bg-primary", "text-white");
            button.classList.add("text-gray-700", "hover:bg-gray-200");
          });
          activeButton.classList.remove("text-gray-700", "hover:bg-gray-200");
          activeButton.classList.add("bg-primary", "text-white");
        }
        function filterProducts(category) {
          productCards.forEach((card) => {
            const productCategory = card
              .querySelector(".text-gray-500")
              .textContent.toLowerCase();
            if (
              category === "all" ||
              productCategory.includes(category.toLowerCase())
            ) {
              card.style.display = "";
            } else {
              card.style.display = "none";
            }
          });
        }
        filterButtons.all.addEventListener("click", function () {
          updateButtonStates(this);
          filterProducts("all");
        });
        filterButtons.women.addEventListener("click", function () {
          updateButtonStates(this);
          filterProducts("women's");
        });
        filterButtons.men.addEventListener("click", function () {
          updateButtonStates(this);
          filterProducts("men's");
        });
        filterButtons.kids.addEventListener("click", function () {
          updateButtonStates(this);
          filterProducts("kids");
        });
      });
    </script>
