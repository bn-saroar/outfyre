    <!-- Price Range Script -->
    <script id="filter-script">
      document.addEventListener("DOMContentLoaded", function () {
        const priceRange = document.getElementById("price-range");
        const priceValue = document.getElementById("price-value");
        const applyFiltersBtn = document.getElementById("apply-filters-btn");
        const loadingIndicator = document.getElementById("loading-indicator");
        const noResults = document.getElementById("no-results");
        const toast = document.createElement("div");
        toast.className =
          "fixed top-24 left-1/2 transform -translate-x-1/2 bg-green-500 text-white px-6 py-3 rounded-lg shadow-lg z-50 opacity-0 transition-opacity duration-300";
        toast.textContent = "";
        document.body.appendChild(toast);
        function showToast() {
          toast.style.opacity = "1";
          setTimeout(() => {
            toast.style.opacity = "0";
          }, 2000);
        }
        const clearFiltersBtn = document.getElementById("clear-filters-btn");
        const productsGrid = document.querySelector(
          ".grid.grid-cols-1.sm\\:grid-cols-2.lg\\:grid-cols-3.gap-6",
        );
        const filterAll = document.getElementById("filter-all");
        const filterWomens = document.getElementById("filter-womens");
        const savedFilterWomens = localStorage.getItem("filterWomens") === "true";
        const savedFilterMens = localStorage.getItem("filterMens") === "true";
        const savedFilterAll = localStorage.getItem("filterAll") === "true";
        if (savedFilterWomens || savedFilterMens || savedFilterAll) {
          filterWomens.checked = savedFilterWomens;
          filterMens.checked = savedFilterMens;
          filterAll.checked = savedFilterAll;
          filterProducts();
        }
        filterAll.addEventListener("change", handleCategoryFilter);
        filterWomens.addEventListener("change", handleCategoryFilter);
        filterMens.addEventListener("change", handleCategoryFilter);
        if (priceRange && priceValue) {
          priceRange.addEventListener("input", function () {
            priceValue.textContent = this.value;
          });
        }
        function getSelectedFilters() {
          const filters = {
            categories: Array.from(
              document.querySelectorAll('input[type="checkbox"]:checked'),
            ).map((cb) => cb.nextElementSibling.nextElementSibling.textContent),
            priceRange: parseInt(document.getElementById("price-range").value),
            color: document
              .querySelector('input[name="color"]:checked')
              ?.parentElement.querySelector(".color-circle").style.backgroundColor,
            size: document
              .querySelector('input[name="size"]:checked')
              ?.parentElement.querySelector(".size-box").textContent,
            rating:
              parseInt(
                document
                  .querySelector('input[name="rating"]:checked')
                  ?.closest("label")
                  .querySelectorAll(".ri-star-fill").length,
              ) || 0,
            showWomensOnly: document.getElementById("filter-womens").checked,
            showMensOnly: document.getElementById("filter-mens").checked,
          };
          return filters;
        }
        function handleCategoryFilter(e) {
          const filterAll = document.getElementById("filter-all");
          const filterWomens = document.getElementById("filter-womens");
          const filterMens = document.getElementById("filter-mens");
          if (e.target.id === "filter-all" && e.target.checked) {
            filterWomens.checked = false;
            filterMens.checked = false;
          } else if (e.target.id === "filter-womens" && e.target.checked) {
            filterAll.checked = false;
            filterMens.checked = false;
          } else if (e.target.id === "filter-mens" && e.target.checked) {
            filterAll.checked = false;
            filterWomens.checked = false;
          }
          localStorage.setItem("filterWomens", filterWomens.checked);
          localStorage.setItem("filterMens", filterMens.checked);
          localStorage.setItem("filterAll", filterAll.checked);
          filterProducts();
        }
        function filterProducts() {
          const filters = getSelectedFilters();
          const loadingIndicator = document.getElementById("loading-indicator");
          const noResults = document.getElementById("no-results");
          const productsGrid = document.querySelector(
            ".grid.grid-cols-1.sm\\:grid-cols-2.lg\\:grid-cols-3.gap-6",
          );
          loadingIndicator.classList.remove("hidden");
          showToast();
          setTimeout(() => {
            const products = Array.from(productsGrid.children);
            const filteredProducts = products.filter((product) => {
              const price = parseFloat(
                product.querySelector(".font-semibold").textContent.replace("$", ""),
              );
              const category = product.querySelector(".text-gray-500").textContent;
              const isWomensProduct = category.includes("Women's");
              const isMensProduct = category.includes("Men's");
              const productRating = product.querySelectorAll(".ri-star-fill").length;
              const productColor =
                product.querySelector(".color-circle")?.style.backgroundColor;
              if (filters.showWomensOnly && !isWomensProduct) return false;
              if (filters.showMensOnly && !isMensProduct) return false;
              if (price > filters.priceRange) return false;
              if (filters.rating && productRating < filters.rating) return false;
              if (filters.color && productColor !== filters.color) return false;
              if (filters.size) {
                const availableSizes = Array.from(
                  product.querySelectorAll(".size-box"),
                ).map((size) => size.textContent);
                if (!availableSizes.includes(filters.size)) return false;
              }
              return true;
            });
            loadingIndicator.classList.add("hidden");
            if (filteredProducts.length === 0) {
              productsGrid.classList.add("hidden");
              noResults.classList.remove("hidden");
            } else {
              productsGrid.classList.remove("hidden");
              noResults.classList.add("hidden");
              products.forEach((product) => product.classList.add("hidden"));
              filteredProducts.forEach((product) =>
                product.classList.remove("hidden"),
              );
            }
            const resultsSection =
              document.getElementById("no-results").parentElement;
            resultsSection.scrollIntoView({ behavior: "smooth", block: "start" });
          }, 1000);
        }
        function clearFilters() {
          document
            .querySelectorAll('input[type="checkbox"]')
            .forEach((cb) => (cb.checked = false));
          document.getElementById("filter-all").checked = true;
          localStorage.setItem("filterWomens", "false");
          localStorage.setItem("filterMens", "false");
          localStorage.setItem("filterAll", "true");
          priceRange.value = 250;
          priceValue.textContent = "250";
          document.querySelector('input[name="color"]').checked = true;
          document.querySelector('input[name="size"]').checked = true;
          document.querySelector('input[name="rating"]').checked = true;
          noResults.classList.add("hidden");
          productsGrid.classList.remove("hidden");
          Array.from(productsGrid.children).forEach((product) => {
            product.classList.remove("hidden");
          });
        }
        if (applyFiltersBtn) {
          applyFiltersBtn.addEventListener("click", filterProducts);
        }
        if (clearFiltersBtn) {
          clearFiltersBtn.addEventListener("click", clearFilters);
        }
      });
    </script>
